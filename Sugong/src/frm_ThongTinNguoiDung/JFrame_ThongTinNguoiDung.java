    public void showData(String maNguoiDung) {
        temp = ndDAO.layThongTinNguoiDung(maNguoiDung);
        setTextFields(temp);
    }
    
    private void setTextFields(ThongTinNguoiDungDTO temp) {
        tfMa.setText(temp.getMaNguoiDung());
        tfCCCD.setText(temp.getCccd());
        tfDiaChi.setText(temp.getDiaChi());
        tfGioiTinh.setText(temp.getGioiTinh());

        // Định dạng ngày tháng thành chuỗi trước khi đặt giá trị cho trường văn bản
        String ngaySinhFormatted = dateFormat.format(temp.getNgaySinh());
        tfNgaySinh.setText(ngaySinhFormatted);
        
        tfTen.setText(temp.getTenNguoiDung());
        tfSoDienThoai.setText(temp.getSdt());
    }
    
